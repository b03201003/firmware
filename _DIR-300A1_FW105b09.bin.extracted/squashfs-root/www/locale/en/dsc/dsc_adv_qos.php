<h1>QoS (Quality of Service)</h1>
<p>
Use this section to configure D-Link's Smart QoS. This Smart QoS improves your VoIP voice
quality or streaming by ensuring that your VoIP or streaming traffic is prioritized over other
network traffic, such as FTP or Web. For best performance, please tick the "lag eliminated"
option to automatically set the priority for your applications.
</p>
